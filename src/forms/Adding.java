    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String u_name = name.getText().toString();
        String u_id   = id_num.getText().toString();
        String u_addr = addr.getText().toString();
        String type = userBox.getSelectedItem().toString();
        String stat = statBox.getSelectedItem().toString();
        String year = jComboBox1.getSelectedItem().toString();
        
        if(u_id.length() != 11 && u_id.length() != 8)
        {   // Checks if id is the right length
            JOptionPane.showMessageDialog(null,"Id Number must be either 8 characters long or 11 characters long.");
        }

        else{
        userObj.user newUser = new userObj.user();
        newUser.setId_num(u_id);
        newUser.setName(u_name);
        newUser.setAddr(u_addr);
        newUser.setStatus(stat);
        newUser.setType(type);
        newUser.setYear(year);
        File fil = new File("center.jpg");
        newUser.setProf_pic(fil.getAbsolutePath());
        File fil2 = new File("left.jpg");
        newUser.setLeft_pic(fil2.getAbsolutePath());
        File fil3 = new File("right.jpg");
        newUser.setRight_pic(fil3.getAbsolutePath());
        DbManager sqlCon = new DbManager();
        sqlCon.addUser(newUser);
        
        fil.delete();
        fil2.delete();
        fil3.delete();
        this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

