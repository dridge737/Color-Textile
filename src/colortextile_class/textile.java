public class textile {
    private String textile_code;
    private String textile_name;

    /**
     * @return the textile_code
     */
    public String getTextile_code() {
        return textile_code;
    }

    /**
     * @param textile_code the textile_code to set
     */
    public void setTextile_code(String textile_code) {
        this.textile_code = textile_code;
    }

    /**
     * @return the textile_name
     */
    public String getTextile_name() {
        return textile_name;
    }

    /**
     * @param textile_name the textile_name to set
     */
    public void setTextile_name(String textile_name) {
        this.textile_name = textile_name;
    }
}
